"""
Batch update all 17 rule reply templates to be professional & contextual.
Run: python3 update_rules.py
"""
import httpx, json, sys, asyncio

BASE = "https://smart-bot-mu.vercel.app"
TOKEN = None

async def login():
    global TOKEN
    async with httpx.AsyncClient() as c:
        r = await c.post(f"{BASE}/api/login", data={"username": "admin", "password": "admin"})
        TOKEN = r.cookies.get("token")
        print(f"Logged in: {r.json()}")

async def get_rules():
    async with httpx.AsyncClient() as c:
        r = await c.get(f"{BASE}/api/rules", cookies={"token": TOKEN})
        return r.json()

async def update_rule(rule_id: int, name: str, reply: str, dm: str, desc: str, keywords_str: str):
    async with httpx.AsyncClient() as c:
        r = await c.put(f"{BASE}/api/rules/{rule_id}",
            cookies={"token": TOKEN},
            data={
                "name": name,
                "keywords": keywords_str,
                "reply_template": reply,
                "dm_template": dm,
                "description": desc,
            })
        return r.json()

