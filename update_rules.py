async def main():
    await login()
    current_rules = await get_rules()
    kw_map = {r["id"]: ",".join(r["keywords"]) for r in current_rules}

    for rid, data in RULES.items():
        if rid not in kw_map:
            print(f"⚠️ Rule {rid} not found, skipping")
            continue
        result = await update_rule(
            rid, data["name"], data["reply"], data["dm"],
            data["desc"], kw_map[rid],
        )
        status = "✅" if result.get("ok") else "❌"
        print(f"{status} #{rid:2d} {data['name']:25s}")

if __name__ == "__main__":
    asyncio.run(main())
